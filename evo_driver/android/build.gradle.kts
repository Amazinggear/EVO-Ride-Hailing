        mavenCentral()
    }
    dependencies {
        classpath("com.google.gms:google-services:4.4.2")
    }
}

allprojects {
    repositories {
        google()
        mavenCentral()
        maven {
            val bgGeo = project.findProject(":flutter_background_geolocation")
            if (bgGeo != null) {
                url = uri("${bgGeo.projectDir}/libs")
            } else {
                url = uri("$rootDir/../build/flutter_background_geolocation/libs")
            }
        }
    }
}

